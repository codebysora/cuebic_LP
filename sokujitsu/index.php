            </p>
            <p class="p-faq-item-a__text">
              ファクタリングは原則として債権ごとの買い取り（都度契約）となるため、違約金は発生しないのが一般的です。ご不安な場合は、現在ご利用中の契約書の「契約期間」や「解除条件」の項目をご確認いただくか、当社の担当者にご相談ください。
            </p>
