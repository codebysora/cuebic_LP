            <p class="p-faq-item-a__text">
              基本的に償還請求権なしの「2社間ファクタリング」で都度（単発）契約をしている場合、解約の申し出は不要です。次回の売掛債権の持ち込みを行わなければ、自動的に契約終了となります。ただし、長期の包括契約などを結んでいる場合は、契約書に記載されている解約手続きをご確認ください。
            </p>
          </div>

